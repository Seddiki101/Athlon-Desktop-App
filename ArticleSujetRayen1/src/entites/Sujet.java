package entites;

/**
 *
 * @author Wasli rayen
 */
public class Sujet {

    private int id;
    private String nom;
    private String description;
    private String img;

    public Sujet() {
    }

    public Sujet(int id, String nom, String description, String img) {
        this.id = id;
        this.nom = nom;
        this.description = description;
        this.img = img;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    @Override
    public String toString() {
        return nom;
    }

}
